function randomInt(min, max) {
    return min + Math.floor(Math.random() * (max - min + 1));
}
function pick(arr) {
    return arr[randomInt(0, arr.length - 1)];
}
/** 2026년 내 임의 시각, 형식: 2026.01.10 오후 3:00 */
function random2026DateTime() {
    const daysInMonth = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31];
    const month = randomInt(1, 12);
    const day = randomInt(1, daysInMonth[month - 1]);
    const hour24 = randomInt(0, 23);
    const minute = randomInt(0, 59);
    let hour12;
    let ampm;
    if (hour24 === 0) {
        hour12 = 12;
        ampm = "오전";
    }
    else if (hour24 < 12) {
        hour12 = hour24;
        ampm = "오전";
    }
    else if (hour24 === 12) {
        hour12 = 12;
        ampm = "오후";
    }
    else {
        hour12 = hour24 - 12;
        ampm = "오후";
    }
    const M = String(month).padStart(2, "0");
    const D = String(day).padStart(2, "0");
    const mm = String(minute).padStart(2, "0");
    return `2026.${M}.${D} ${ampm} ${hour12}:${mm}`;
}
function collectTextNodes(nodes) {
    const out = [];
    for (const node of nodes) {
        if (node.type === "TEXT") {
            out.push(node);
        }
        else if ("children" in node) {
            for (const child of node.children) {
                out.push(...collectTextNodes([child]));
            }
        }
    }
    return out;
}
function valueForKind(kind) {
    switch (kind) {
        case "name":
            return pick(NAMES);
        case "company":
            return pick(COMPANIES);
        case "team":
            return pick(TEAMS);
        case "datetime":
            return random2026DateTime();
        case "tax":
            return pick(TAX_DUMMY_BLOCKS);
    }
}
async function applyFillToSelection(kind) {
    const roots = figma.currentPage.selection;
    if (roots.length === 0) {
        figma.notify("텍스트 레이어를 선택해 주세요.", { error: true });
        return;
    }
    const textNodes = collectTextNodes(roots);
    if (textNodes.length === 0) {
        figma.notify("선택한 항목에 텍스트 레이어가 없습니다.", { error: true });
        return;
    }
    const fonts = new Set();
    for (const node of textNodes) {
        const fn = node.fontName;
        if (fn === figma.mixed)
            continue;
        fonts.add(`${fn.family}:::${fn.style}`);
    }
    for (const key of fonts) {
        const [family, style] = key.split(":::");
        await figma.loadFontAsync({ family, style });
    }
    for (const node of textNodes) {
        const fn = node.fontName;
        if (fn === figma.mixed)
            continue;
        await figma.loadFontAsync(fn);
        node.characters = valueForKind(kind);
    }
    figma.notify(`${textNodes.length}개 텍스트를 채웠습니다.`);
}
figma.showUI(__html__, { width: 240, height: 350, themeColors: true });
/** ui.html: 이름→fill-name, 회사명→fill-company, 팀 이름→fill-team, 날짜 시간→fill-datetime, 긴 문장→fill-tax */
figma.ui.onmessage = (msg) => {
    if (!msg || typeof msg.type !== "string")
        return;
    const run = async () => {
        if (msg.type === "fill-name")
            await applyFillToSelection("name");
        else if (msg.type === "fill-company")
            await applyFillToSelection("company");
        else if (msg.type === "fill-team")
            await applyFillToSelection("team");
        else if (msg.type === "fill-datetime")
            await applyFillToSelection("datetime");
        else if (msg.type === "fill-tax")
            await applyFillToSelection("tax");
    };
    void run().catch((e) => {
        const message = e instanceof Error ? e.message : String(e);
        figma.notify(`오류: ${message}`, { error: true });
    });
};
